# Se asigna un valor de texto a la variable y se imprime
nombre = "Catherine"
print(nombre)

# La variable puede cambiar de valor y se muestra el nuevo contenido
nombre = "Laura"
print(nombre)

# Se crean variables numéricas y se suman para mostrar el resultado
edad = 20
edad2 = 20
print(edad + edad2)

# Se pide un dato al usuario y se imprime junto a un texto fijo
nombre = input("Dime tu nombre: ")
print("Tu nombre es: " + nombre)

# Se concatenan dos variables de texto para formar una frase
palabra1 = "Hola "
palabra2 = "Mundo"
frase = palabra1 + palabra2
print(frase)

# Se suman dos variables numéricas y se imprime el resultado
numero1 = 15
numero2 = 10
suma = numero1 + numero2
print(suma)


