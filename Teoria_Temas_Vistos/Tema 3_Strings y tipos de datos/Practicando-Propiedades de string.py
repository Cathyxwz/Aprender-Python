# Concatenar strings con +
nombre = "Cathe"
nombre2 = "rine"
print(nombre + nombre2) # Resultado: "Catherine"

# Multiplicar strings con *
nombre = "10"
print(nombre * 10) # Resultado: "10101010101010101010"

# Strings en varias líneas usando triple comillas
poema = """Mil pequeños peces blancos
como si hirviera
el color de algua"""
print(poema)

# Consultar si una palabra existe dentro de un string con 'in'
print("agua" in poema) # Resultado: True

# Largo de un string con len()
print(len(poema)) # Devuelve la cantidad de caracteres del string
