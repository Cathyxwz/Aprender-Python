# Imprime un texto simple en pantalla
print("Hola mundo")

# Concatenación de cadenas: une "Hola " con 'Cathy'
print("Hola " + 'Cathy')

# \n es un salto de línea: imprime dos frases en líneas distintas
print("Esta es una linea\nY esta otra linea")

# \\ imprime una barra invertida literal
print("Este signo \\ es una barra invertida")

# \t es un tabulador: separa los elementos con espacios grandes
print("\tA\tB\tC")

