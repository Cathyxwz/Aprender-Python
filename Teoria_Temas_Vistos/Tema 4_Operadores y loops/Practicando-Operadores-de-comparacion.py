# Operadores de comparación en Python

# Igual a (==) → compara si dos valores son iguales
mi_bool = 10 == 25
print(mi_bool)   # False, porque 10 no es igual a 25

# Diferente a (!=) → compara si dos valores son distintos
mi_bool2 = 100 != 100
print(mi_bool2)  # False, porque 100 sí es igual a 100

# Mayor que (>) → compara si el valor de la izquierda es mayor
mi_bool3 = 100 > 100
print(mi_bool3)  # False, porque 100 no es mayor que 100 (son iguales)

# Menor que (<) → compara si el valor de la izquierda es menor
mi_bool4 = 100 < 100
print(mi_bool4)  # False, porque 100 no es menor que 100 (son iguales)
