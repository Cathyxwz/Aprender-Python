# Redondeo directo desde print: se calcula la división y se redondea al entero más cercano
print(round(90/7))

# Redondeo guardando el resultado en una variable
resultado = round(90/7)
print(resultado)

# Redondeo especificando el número de decimales (en este caso, 2)
valor = round(95.6666, 2)
print(valor)
