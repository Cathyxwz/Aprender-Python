# Acceder a un carácter por índice: se usa [] indicando la posición (empieza en 0)
mi_texto = "Esta es una prueba"
resultado = mi_texto[9]   # Obtiene el carácter en la posición 9
print(resultado)

# Buscar la posición de un carácter o subcadena: se usa .index("texto")
mi_texto = "Esta es una prueba"
resultado = mi_texto.index("n")   # Devuelve la posición donde aparece la letra "n"
print(resultado)
